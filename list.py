c=[10,20,30,40,50]
print(c[1])
print(c[-1])
print(c[1:3])#it is called traversing
print(c[1:7])#no error
#print(c[7])#error
print(c[:])#traverse all values
print(c[-4:-1])#sarting index is less then ending index(n-1)concept
print(c[3:-1])
print(c[1:])#start from index 1 and traverse till last index
print(c[:-1])#start from 0 index
print(c[0:])#start from 0 index and traverse till end index
print(c[0:4:2])#it is a step value, which means gap to 2 elements


